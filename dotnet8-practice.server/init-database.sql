DROP TABLE T_EMPLOYEE;
DROP TABLE T_DEPARTMENT;

CREATE TABLE T_DEPARTMENT (
    DEPARTMENT_ID INT PRIMARY KEY IDENTITY(100,1),
    DEPARTMENT_NAME VARCHAR(50) NOT NULL UNIQUE
);

CREATE TABLE T_EMPLOYEE (
    EMPLOYEE_ID INT PRIMARY KEY IDENTITY(100,1),
    EMPLOYEE_NAME VARCHAR(50) NOT NULL,
    EMPLOYEE_SALARY DECIMAL(18,2) NOT NULL,
	EMPLOYEE_GENDER VARCHAR(20) NOT NULL,
	EMPLOYEE_AGE INT NOT NULL,
	EMPLOYEE_CONTACT_COUNTRY_CODE INT NOT NULL,
	EMPLOYEE_CONTACT_NO INT NOT NULL,
	EMPLOYEE_ADDRESS_LINE_1 NVARCHAR(75) NOT NULL,
	EMPLOYEE_ADDRESS_LINE_2 NVARCHAR(75) NULL,
	EMPLOYEE_ADDRESS_UNIT_NO VARCHAR(15) NULL,
	EMPLOYEE_ADDRESS_POSTAL_CODE INT NOT NULL,
	EMPLOYEE_ADDRESS_COUNTRY VARCHAR(30) NOT NULL,
	EMPLOYEE_ADDRESS_CITY VARCHAR(30) NULL,
	EMPLOYEE_ADDRESS_STATE VARCHAR(30) NULL,
    EMPLOYEE_DEPARTMENT_ID INT NOT NULL,
    CONSTRAINT FK_EMPLOYEE_DEPARTMENT_ID FOREIGN KEY (EMPLOYEE_DEPARTMENT_ID)
        REFERENCES T_DEPARTMENT(DEPARTMENT_ID) ON DELETE CASCADE
);

